# Create ConfigMap for the dashboard
kubectl create configmap grafana-custom-dashboard --from-file=custom-dashboard.json

# Update Grafana deployment to mount the custom dashboard
kubectl patch deployment grafana --patch '{"spec":{"template":{"spec":{"volumes":[{"name":"custom-dashboard","configMap":{"name":"grafana-custom-dashboard"}}],"containers":[{"name":"grafana","volumeMounts":[{"mountPath":"/var/lib/grafana/dashboards/custom","name":"custom-dashboard"}]}]}}}}'

# Create ConfigMap for dashboard provisioning
cat > dashboard-provisioning.yaml << 'EOF'
apiVersion: v1
kind: ConfigMap
metadata:
  name: grafana-dashboard-provisioning
data:
  dashboards.yaml: |
    apiVersion: 1
    providers:
    - name: 'default'
      orgId: 1
      folder: ''
      type: file
      disableDeletion: false
      updateIntervalSeconds: 10
      options:
        path: /var/lib/grafana/dashboards/custom
EOF

# Apply the dashboard provisioning ConfigMap
kubectl apply -f dashboard-provisioning.yaml

# Update Grafana deployment to mount the provisioning config
kubectl patch deployment grafana --patch '{"spec":{"template":{"spec":{"volumes":[{"name":"dashboard-provisioning","configMap":{"name":"grafana-dashboard-provisioning"}}],"containers":[{"name":"grafana","volumeMounts":[{"mountPath":"/etc/grafana/provisioning/dashboards","name":"dashboard-provisioning"}]}]}}}}'

# Restart Grafana to apply changes
kubectl rollout restart deployment grafana

echo "Custom dashboard has been created and provisioned in Grafana."
echo "Wait for Grafana to restart, then access it using: minikube service grafana --url"
